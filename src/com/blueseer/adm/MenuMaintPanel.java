    public void getMenu(String mykey) {
       
        try {

            Class.forName(bsmf.MainFrame.driver).newInstance();
            bsmf.MainFrame.con = DriverManager.getConnection(bsmf.MainFrame.url + bsmf.MainFrame.db, bsmf.MainFrame.user, bsmf.MainFrame.pass);
            try {
                Statement st = bsmf.MainFrame.con.createStatement();
                ResultSet res = null;
                int i = 0;
                res = st.executeQuery("select * from menu_mstr where menu_id = " + "'" + mykey + "'" + ";");
                while (res.next()) {
                    i++;
                    tbcode.setText(res.getString("menu_id"));
                    tbdesc.setText(res.getString("menu_desc"));
                    tbpanel.setText(res.getString("menu_panel"));
                    tbnavcode.setText(res.getString("menu_navcode"));
                    if (res.getString("menu_type").compareTo("JMenu") == 0) {
                       cbparent.setSelected(true);
                    } else {
                       cbparent.setSelected(false); 
                    }
                }
               
                if (i > 0) {
                    enableAll();
                    btadd.setEnabled(false);
                }
               

            } catch (SQLException s) {
                s.printStackTrace();
                bsmf.MainFrame.show("Unable to retrieve menu_mstr");
            }
            bsmf.MainFrame.con.close();
        } catch (Exception e) {
            e.printStackTrace();
        }

    }
    
    
