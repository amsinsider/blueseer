    private void btaddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btaddActionPerformed
       try {

            Class.forName(bsmf.MainFrame.driver).newInstance();
            bsmf.MainFrame.con = DriverManager.getConnection(bsmf.MainFrame.url + bsmf.MainFrame.db, bsmf.MainFrame.user, bsmf.MainFrame.pass);
            try {
                Statement st = bsmf.MainFrame.con.createStatement();
                ResultSet res = null;
                boolean proceed = true;
                int i = 0;
                String menutype = "JMenuItem";
                // check the site field
                if (tbcode.getText().isEmpty()) {
                    proceed = false;
                    bsmf.MainFrame.show("Must enter a Menu ID");
                    tbcode.requestFocus();
                }
                if (! OVData.isValidPanel(tbpanel.getText()) && ! cbparent.isSelected()) {
                    proceed = false;
                    bsmf.MainFrame.show("Must enter a valid Class ID");
                    tbpanel.requestFocus();
                }
                
                 if (cbparent.isSelected()) {
                    menutype = "JMenu";
                }
                
                
                if (proceed) {

                    res = st.executeQuery("SELECT menu_id FROM  menu_mstr where menu_id = " + "'" + tbcode.getText() + "'" + ";");
                    while (res.next()) {
                        i++;
                    }
                    if (i == 0) {
                        st.executeUpdate("insert into menu_mstr "
                            + "(menu_id, menu_desc, menu_type, menu_panel, menu_navcode ) "
                            + " values ( " + "'" + tbcode.getText().toString() + "'" + ","
                            + "'" + tbdesc.getText().toString() + "'" + ","
                            + "'" + menutype + "'" + ","
                            + "'" + tbpanel.getText().toString() + "'" + "," 
                            + "'" + tbnavcode.getText().toString() + "'"
                            + ")"
                            + ";");
                        bsmf.MainFrame.show("Added Menu Record");
                    } else {
                        bsmf.MainFrame.show("Menu Record Already Exists");
                    }

                    // now assign menu to admin user as default
                    String myreturn = OVData.addMenuToUser(tbcode.getText().toString(), "Admin");
                    
                   initvars("");
                   menumap.clear();
                   menumap = bsmf.MainFrame.getmenulistmap();
                } // if proceed
            } catch (SQLException s) {
                s.printStackTrace();
                bsmf.MainFrame.show("Unable to Add to menu_mstr");
            }
            bsmf.MainFrame.con.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btaddActionPerformed

    private void btupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btupdateActionPerformed
       try {
            boolean proceed = true;
            Class.forName(bsmf.MainFrame.driver).newInstance();
            bsmf.MainFrame.con = DriverManager.getConnection(bsmf.MainFrame.url + bsmf.MainFrame.db, bsmf.MainFrame.user, bsmf.MainFrame.pass);
            try {
                Statement st = bsmf.MainFrame.con.createStatement();
                String menutype = "JMenuItem";   
                
                // check the code field
                if (tbcode.getText().isEmpty()) {
                    proceed = false;
                    bsmf.MainFrame.show("Must enter a menu ID");
                }
                
                if (cbparent.isSelected()) {
                    menutype = "JMenu";
                }
                
                if (proceed) {
                    st.executeUpdate("update menu_mstr " 
                            + "set menu_desc = " + "'" + tbdesc.getText() + "'" + ","
                            + " menu_type = " + "'" + menutype + "'" + ","
                            + "menu_panel = " + "'" + tbpanel.getText() + "'" + ","
                            + "menu_navcode = " + "'" + tbnavcode.getText() + "'"
                            + " where menu_id = " + "'" + tbcode.getText() + "'"                             
                            + ";");
                    bsmf.MainFrame.show("Updated Menu");
                    initvars("");
                    menumap.clear();
                   menumap = bsmf.MainFrame.getmenulistmap();
                } 
         
            } catch (SQLException s) {
                bsmf.MainFrame.show("Problem updating menu_mstr");
            }
            bsmf.MainFrame.con.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btupdateActionPerformed

