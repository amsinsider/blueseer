    private void btaddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btaddActionPerformed
       try {

            Class.forName(bsmf.MainFrame.driver).newInstance();
            bsmf.MainFrame.con = DriverManager.getConnection(bsmf.MainFrame.url + bsmf.MainFrame.db, bsmf.MainFrame.user, bsmf.MainFrame.pass);
            try {
                Statement st = bsmf.MainFrame.con.createStatement();
                ResultSet res = null;
                boolean proceed = true;
                int i = 0;
                
                // check the site field
                if (tbcode.getText().isEmpty()) {
                    proceed = false;
                    bsmf.MainFrame.show("Must enter a printer code");
                    return;
                }
                
               

                    res = st.executeQuery("SELECT prt_id, prt_ip FROM  prt_mstr where prt_id = " + "'" + tbcode.getText() + "'" + ";");
                    while (res.next()) {
                        bsmf.MainFrame.show("Already have printer with that Code");
                        proceed = false;
                        return;
                    }
                    res = st.executeQuery("SELECT prt_id, prt_ip FROM  prt_mstr where prt_ip = " + "'" + tbip.getText() + "'" + ";");
                    while (res.next()) {
                        bsmf.MainFrame.show("Already have printer with that IP");
                        proceed = false;
                        return;
                    }
                    
                    if (proceed) {
                        st.executeUpdate("insert into prt_mstr "
                            + " (prt_id, prt_desc, prt_type, prt_ip, prt_port ) "
                            + " values ( " + "'" + tbcode.getText().toString() + "'" + ","
                            + "'" + tbdesc.getText().toString() + "'" + ","
                            + "'" + ddtype.getSelectedItem().toString() + "'" + ","
                                + "'" + tbip.getText().toString() + "'" + ","
                                + "'" + tbport.getText().toString() + "'"
                            + ")"
                            + ";");
                        bsmf.MainFrame.show("Added Printer Record");
                    } else {
                        bsmf.MainFrame.show("Could not Add Printer Record");
                    }

                   initvars("");
                   
               
            } catch (SQLException s) {
                MainFrame.bslog(s);
                bsmf.MainFrame.show("Unable to Add to prt_mstr");
            }
            bsmf.MainFrame.con.close();
        } catch (Exception e) {
            MainFrame.bslog(e);
        }
    }//GEN-LAST:event_btaddActionPerformed

    private void btupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btupdateActionPerformed
       try {
            boolean proceed = true;
            Class.forName(bsmf.MainFrame.driver).newInstance();
            bsmf.MainFrame.con = DriverManager.getConnection(bsmf.MainFrame.url + bsmf.MainFrame.db, bsmf.MainFrame.user, bsmf.MainFrame.pass);
            try {
                Statement st = bsmf.MainFrame.con.createStatement();
                   
                // check the code field
                if (tbcode.getText().isEmpty()) {
                    proceed = false;
                    bsmf.MainFrame.show("Must enter a code");
                }
                
                if (proceed) {
                    st.executeUpdate("update prt_mstr set prt_desc = " + "'" + tbdesc.getText() + "'" + ","
                            + "prt_ip = " + "'" + tbip.getText() + "'" + ","
                            + "prt_port = " + "'" + tbport.getText() + "'" + ","
                            + "prt_type = " + "'" + ddtype.getSelectedItem().toString() + "'" 
                            + " where prt_id = " + "'" + tbcode.getText() + "'"                             
                            + ";");
                    bsmf.MainFrame.show("Updated Printer");
                    initvars("");
                } 
         
            } catch (SQLException s) {
                MainFrame.bslog(s);
                bsmf.MainFrame.show("Problem updating prt_mstr");
            }
            bsmf.MainFrame.con.close();
        } catch (Exception e) {
            MainFrame.bslog(e);
        }
    }//GEN-LAST:event_btupdateActionPerformed

