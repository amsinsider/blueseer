    private void btaddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btaddActionPerformed
       try {

            Class.forName(bsmf.MainFrame.driver).newInstance();
            bsmf.MainFrame.con = DriverManager.getConnection(bsmf.MainFrame.url + bsmf.MainFrame.db, bsmf.MainFrame.user, bsmf.MainFrame.pass);
            try {
                Statement st = bsmf.MainFrame.con.createStatement();
                ResultSet res = null;
                boolean proceed = true;
                int i = 0;
                
                // check the site field
                if (tbtpid.getText().isEmpty()) {
                    proceed = false;
                    bsmf.MainFrame.show("Must enter a TP ID");
                }
              
               
                
                if (proceed) {

                    res = st.executeQuery("SELECT editp_id FROM  editp_mstr where editp_id = " + "'" + tbtpid.getText() + "'" +
                                           ";");
                    while (res.next()) {
                        i++;
                    }
                    if (i == 0) {
                        st.executeUpdate("insert into editp_mstr "
                            + "(editp_id, editp_name, editp_contact, editp_web, editp_helpdesk ) "
                            + " values ( " + "'" + tbtpid.getText().toString() + "'" + ","
                            + "'" + tbdesc.getText().toString() + "'" + ","
                                + "'" + tbcontact.getText().toString() + "'" + ","
                                + "'" + tbweb.getText().toString() + "'" + "," 
                                + "'" + tbhelpdesk.getText().toString() + "'" 
                            + ")"
                            + ";");
                        bsmf.MainFrame.show("Added TPID Record");
                    } else {
                        bsmf.MainFrame.show("TPID Already Exists");
                    }

                   initvars("");
                   
                } // if proceed
            } catch (SQLException s) {
                MainFrame.bslog(s);
                bsmf.MainFrame.show("Unable to Add to editp_mstr");
            }
            bsmf.MainFrame.con.close();
        } catch (Exception e) {
            MainFrame.bslog(e);
        }
    }//GEN-LAST:event_btaddActionPerformed

    private void btupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btupdateActionPerformed
       try {
            boolean proceed = true;
            Class.forName(bsmf.MainFrame.driver).newInstance();
            bsmf.MainFrame.con = DriverManager.getConnection(bsmf.MainFrame.url + bsmf.MainFrame.db, bsmf.MainFrame.user, bsmf.MainFrame.pass);
            try {
                Statement st = bsmf.MainFrame.con.createStatement();
                   
                // check the code field
                if (tbtpid.getText().isEmpty()) {
                    proceed = false;
                    bsmf.MainFrame.show("Must enter a TP ID");
                }
              
                
                if (proceed) {
                    st.executeUpdate("update editp_mstr set editp_name = " + "'" + tbdesc.getText() + "'" + ","
                            + "editp_contact = " + "'" + tbcontact.getText() + "'" + ","
                            + "editp_web = " + "'" + tbweb.getText() + "'" + ","
                            + "editp_helpdesk = " + "'" + tbhelpdesk.getText() + "'"
                            + " where editp_id = " + "'" + tbtpid.getText() + "'"     
                            + ";");
                    bsmf.MainFrame.show("Updated TPID");
                    initvars("");
                } 
         
            } catch (SQLException s) {
                MainFrame.bslog(s);
                bsmf.MainFrame.show("Problem updating editp_mstr");
            }
            bsmf.MainFrame.con.close();
        } catch (Exception e) {
            MainFrame.bslog(e);
        }
    }//GEN-LAST:event_btupdateActionPerformed

