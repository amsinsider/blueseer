    public void getTPID(String tpid) {
        
        try {

            Class.forName(bsmf.MainFrame.driver).newInstance();
            bsmf.MainFrame.con = DriverManager.getConnection(bsmf.MainFrame.url + bsmf.MainFrame.db, bsmf.MainFrame.user, bsmf.MainFrame.pass);
            try {
                Statement st = bsmf.MainFrame.con.createStatement();
                ResultSet res = null;
                int i = 0;
                res = st.executeQuery("select * from editp_mstr where editp_id = " + "'" + tpid + "'" +
                                       ";");
                while (res.next()) {
                    i++;
                    tbcontact.setText(res.getString("editp_contact"));
                    tbdesc.setText(res.getString("editp_name"));
                    tbtpid.setText(res.getString("editp_id"));
                    tbweb.setText(res.getString("editp_web"));
                    tbhelpdesk.setText(res.getString("editp_helpdesk"));
                   
                }
               
                
                if (i > 0) {
                    enableAll();
                  btadd.setEnabled(false);
                }
                   

            } catch (SQLException s) {
                MainFrame.bslog(s);
                bsmf.MainFrame.show("Unable to retrieve editp_mstr");
            }
            bsmf.MainFrame.con.close();
        } catch (Exception e) {
            MainFrame.bslog(e);
        }

    }
    
