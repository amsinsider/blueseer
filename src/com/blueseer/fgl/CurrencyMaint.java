    public void getCurrencyCode(String mycode) {
        
        try {

            Class.forName(bsmf.MainFrame.driver).newInstance();
            bsmf.MainFrame.con = DriverManager.getConnection(bsmf.MainFrame.url + bsmf.MainFrame.db, bsmf.MainFrame.user, bsmf.MainFrame.pass);
            try {
                Statement st = bsmf.MainFrame.con.createStatement();
                ResultSet res = null;
                int i = 0;
                res = st.executeQuery("select * from cur_mstr where cur_id = " + "'" + mycode + "'" + ";");
                while (res.next()) {
                    i++;
                    tbdesc.setText(res.getString("cur_desc"));
                    tbcode.setText(res.getString("cur_id"));
                  
                }
               
                 if (i > 0) {
                    enableAll();
                    tbcode.setEnabled(false);
                    btadd.setEnabled(false);
                }

            } catch (SQLException s) {
                s.printStackTrace();
                bsmf.MainFrame.show("Unable to retrieve cur_mstr");
            }
            bsmf.MainFrame.con.close();
        } catch (Exception e) {
            e.printStackTrace();
        }

    }
    
